/*
Problem: Swap Two Numbers
Method:Built-in Swap

Time Complexity: O(1)
Space Complexity: O(1)
*/

#include <iostream>
using namespace std;

int main() {
    int a = 2, b = 3;
    cout << "a = " << a << " b = " << b << endl;   
  
    swap(a, b);
  
    cout << "a = " << a << " b = " << b << endl;
    return 0;
}
